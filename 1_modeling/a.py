# Просте  симетричне випадкове блукання стартує  з 0. 

# Зроблено 100 кроків. 

# a) Порахувати кількість кроків, протягом  яких процес знаходився вище ОХ.  
# Порахувати (емпіричну) ймовірність такої події (тб. поділити на кількість всіх кроків, тб. 100). Порівняти з теоретичною імовірністю. 

import random

# Кількість кроків
n = 100
# Лічильник для кількості разів, коли процес був вище ОХ
above_x_count = 0

# Моделюємо просте симетричне випадкове блукання
current_position = 0
for _ in range(n):
    # Генеруємо випадковий крок (вверх або вниз)
    step = random.choice([-1, 1])
    current_position += step
    # Перевіряємо, чи процес знаходиться вище ОХ
    if current_position > 0:
        above_x_count += 1

# Емпірична ймовірність
empirical_probability = above_x_count / n

print(f"Емпірична ймовірність: {empirical_probability}")
print(f"Теоретична ймовірність: 0.5")

# відповідь: Емпірична ймовірність: 0.91 Теоретична ймовірність: 0.5
